function vuscaColabs(){
    var disp = document.getElementById('resultadobusca')
    if(disp.style.display == '' || disp.style.display == 'none'){
      disp.style.display = 'block';
    }
}

function fechabusca(){
    var disp = document.getElementById('resultadobusca')
    disp.style.display = 'none';
}
